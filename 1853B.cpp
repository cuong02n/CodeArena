int MOD = 1e9 + 7;
const int N = 40;
int f[N];

void precal() {
    f[0] = 0;
    f[1] = 1;
    for (int i = 2; i < N - 1; i++) {
        f[i] = f[i - 1] + f[i - 2];
    }
}
void solve(bool verbose = false) {
    int n;
    cin >> n;
    int k;
    cin >> k;
    if (k > 30 && n != 0) {
        cout << 0 << endl;
        return;
    } else if (k > 30) {
        cout << 1 << endl;
    }
    int a = f[k - 2];
    int b = f[k - 1];
    int res = 0;
    for (int x = 0; x <= n / a; x++) {
        long double y = (1.0 * n - x * a) / b;
        if (x > (int)y) {
            break;
        }
        if (y == (int)y) {
            res++;
        }
    }
    cout << res << endl;
}

